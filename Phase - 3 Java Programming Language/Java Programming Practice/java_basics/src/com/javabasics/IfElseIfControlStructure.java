package com.javabasics;

public class IfElseIfControlStructure {
	public static void main(String args[])
	{
		int color = 1;
		if(color == 1)
		{
			System.out.println("Blue");
		}
		else if(color == 2)
		{
			System.out.println("Sky Blue");
		}
		else if(color == 3)
		{
			System.out.println("Red");
		}
	}
}
