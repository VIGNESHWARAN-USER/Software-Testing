package com.javabasics;

public class SwitchCase {
	public static void main(String args[])
	{
		int val = 'a';
		switch(val)
		{
			case 'a':
				System.out.println("A");
				break;
			case 'b':
				System.out.println("B");
				break;
			case 'c':
				System.out.println("C");
				break;
			case 'd':
				System.out.println("D");
				break;
		}
	}
}
